			Kitten Factory is a local ski manufacturing company that specializes in customized carbon fiber skis. The owners and operators are local entrepreneurs with mechanical engineering degrees from the University of Utah with over 14 years of combined ski making experience. The products produced by Kitten Factory are made using carbon fiber which makes them lighter than other fiberglass skis found on the market. The lighter weight will help consumers have a more enjoyable ride no matter the terrain. In addition to providing top of the line products, the company is concerned with adding to the local economy.  They offer a wide variety of skis including, park, powder, and touring skis with hybrid wood and carbon lay ups to full carbon lay ups making their skis some of the most durables skis on the market. Over the past 6 years the owners, employees and team riders have been riding the skis they make every day in order to create the best possible product for the consumer that will withstand the test of time. Kitten Factory Skis are truly a rider owned and operated company looking to create the highest quality product available for the skier who is looking to go into the park and backcountry and really test their equipment for what it was meant for. The local winter recreation industry has always been a staple of the Utah market and Kitten Factory is slowly making a name for itself.
